    public static String getStandardDate(Long timeMillis) {
        StringBuffer sb = new StringBuffer();

        long time = System.currentTimeMillis() - timeMillis;
        //秒前
        long mill = (long) Math.ceil(time / 1000);
        // 分钟前
        long minute = (long) Math.ceil(time / 60 / 1000.0f);
        // 小时
        long hour = (long) Math.ceil(time / 60 / 60 / 1000.0f);
        // 天前
        long day = (long) Math.ceil(time / 24 / 60 / 60 / 1000.0f);

        if (day - 1 > 2) {
            return getStrTime("" + timeMillis / 1000);
        } else if (day - 1 == 2) {
            sb.append("前天");
        } else if (day - 1 == 1) {
            sb.append(day + "昨天");
        } else if (hour - 1 > 0) {
            if (hour >= 24) {
                sb.append("1天");
            } else {
                sb.append(hour + "小时");
            }
        } else if (minute - 1 > 0) {
            if (minute == 60) {
                sb.append("1小时");
            } else {
                sb.append(minute + "分钟");
            }
        } else if (mill - 1 > 0) {
            if (mill == 60) {
                sb.append("1分钟");
            } else {
                sb.append(mill + "秒");
            }
        } else {
            sb.append("刚刚");
        }
        if (!sb.toString().equals("刚刚")) {
            sb.append("前");
        }
        return sb.toString();
    }

